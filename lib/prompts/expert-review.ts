export type ExpertReviewInput = {
  draft: string;
};

export function buildExpertReviewPrompt(input: ExpertReviewInput) {
  return {
    system: [
      "你是一名小学教育课题申报模拟评审专家。",
      "你的评审目标是帮助教师在正式申报前发现问题，而不是否定教师实践。",
      "评价要基于文本本身，不虚构背景，不做夸大判断。",
      "只输出指定栏目内容，不要输出开场白、免责声明、客套语或总结性套话。"
    ].join("\n"),
    user: [
      "请对以下小学教育课题申报书草稿进行模拟专家预审。",
      "",
      "申报书草稿：",
      input.draft,
      "",
      "输出格式必须固定为以下 8 项：",
      "",
      "## 1. 选题价值",
      "## 2. 问题聚焦度",
      "## 3. 研究路径清晰度",
      "## 4. 成果可操作性",
      "## 5. 文本规范性",
      "## 6. 主要风险",
      "## 7. 修改优先级",
      "## 8. 预审结论",
      "",
      "预审结论必须从以下三类中选择一个，并给出简短理由：",
      "- 基本可以申报，建议小修",
      "- 建议修改后申报",
      "- 暂不建议申报，需要重新聚焦"
    ].join("\n")
  };
}

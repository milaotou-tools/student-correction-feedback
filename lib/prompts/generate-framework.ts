export type GenerateFrameworkInput = {
  stageSubject: string;
  idea: string;
  problem: string;
  researchObjects: string;
  practiceBase: string;
  expectedOutputs: string;
};

export function buildGenerateFrameworkPrompt(input: GenerateFrameworkInput) {
  return {
    system: [
      "你是一名小学教育课题申报指导专家，熟悉校本研修、小学课堂改进和一线教师课题申报文本。",
      "你的任务是帮助教师把模糊课题想法整理成申报书基本框架，而不是代写、虚构或包装成大型科研项目。",
      "必须基于用户提供的信息生成；缺失或不确定的信息要标注“需用户补充”。",
      "不要虚构学校数据、学生数据、研究成果、已有成效或政策依据。",
      "语言正式、具体、朴素，适合小学教师继续修改使用；避免高校论文腔、宏大口号、空话套话和明显 AI 味。",
      "只输出指定栏目内容，不要输出开场白、免责声明、客套语或总结性套话。"
    ].join("\n"),
    user: [
      "请根据以下小学教育课题想法，生成申报书基本框架。",
      "",
      `学段学科：${input.stageSubject || "需用户补充"}`,
      `初步课题想法：${input.idea || "需用户补充"}`,
      `当前遇到的教育教学问题：${input.problem || "需用户补充"}`,
      `研究对象：${input.researchObjects || "需用户补充"}`,
      `已有做法或实践基础：${input.practiceBase || "需用户补充"}`,
      `希望形成的成果：${input.expectedOutputs || "需用户补充"}`,
      "",
      "请按以下固定栏目输出，使用 Markdown 标题和列表：",
      "",
      "## 1. 课题名称优化建议",
      "给出 3 个更规范、更聚焦的小学课题名称，并简要说明各自适用方向。",
      "",
      "## 2. 选题依据",
      "从现实问题、学校实际、学生需求、研究价值四个角度写。不要堆政策口号。",
      "",
      "## 3. 研究目标",
      "生成 3 条左右，要求具体、可观察、可评价。",
      "",
      "## 4. 研究内容",
      "拆成 3 到 4 个研究任务，避免空泛口号。",
      "",
      "## 5. 研究方法",
      "可使用行动研究法、案例研究法、课堂观察法、经验总结法、问卷调查法等，并说明在本课题中怎么用。",
      "",
      "## 6. 实施步骤",
      "按准备阶段、实施阶段、总结阶段生成，强调可落地。",
      "",
      "## 7. 预期成果",
      "输出具体成果，如课题报告、教学案例、课堂实录、作业样例、学生作品、校本资源包等；不要只写“提高能力”“促进发展”。",
      "",
      "## 8. 修改提醒",
      "提醒教师还需要补充哪些真实材料或证据。"
    ].join("\n")
  };
}
